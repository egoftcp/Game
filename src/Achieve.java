
public class Achieve {
	
	private int pId;
	private int gId;
	private int aId;

	public Achieve(int pId, int gId, int aId) {
		super();
		this.pId = pId;
		this.gId = gId;
		this.aId = aId;
	}

	public int getpId() {
		return pId;
	}

	public void setpId(int pId) {
		this.pId = pId;
	}

	public int getgId() {
		return gId;
	}

	public void setgId(int gId) {
		this.gId = gId;
	}

	public int getaId() {
		return aId;
	}

	public void setaId(int aId) {
		this.aId = aId;
	}

}
